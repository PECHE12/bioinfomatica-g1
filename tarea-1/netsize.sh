Tutorial de conceptos básicos  para aprender el uso de  Python.

##Cómo debes pensar antes de aprender a programar en Python.
- A veces es difícil encontrar motivación, especialmente cuando nos enfrentamos a obstáculos que nos cambian la vida.
- Los pensamientos son muy poderosos  y pueden  evitar que cumplas las  metas propuestas.
- Puedo hacer lo que sea. Es una frase muy simple, pero te ayuda a recordar que realmente tienes la libertad de lograr lo que te propongas.
- Nunca es tarde. Sin importar que tan “viejo” creas estar, siempre se puede hacer un alto y cambiar de dirección.

## Conceptos básicos para aprender a programar Python.
*  Manipulación de cadenas.
*  Manipulación de números.
*  Manipulado de fecha y hora.
*  Tratar con el sistema de archivos.
 
